export const getFeaturedArtwork = (): Artwork => {
  // Return a random featured and unsold artwork
  const featuredUnsoldArtworks = artworks.filter((p) => p.featured && !p.sold);
  return featuredUnsoldArtworks[Math.floor(Math.random() * featuredUnsoldArtworks.length)];
};

export const getArtworkById = (id: string): Artwork | undefined => {
  return artworks.find((artwork) => artwork.id === id);
};

export const getArtworksByCollection = (collection: string): Artwork[] => {
  if (collection === 'all') return artworks;
  return artworks.filter(
    (artwork) => artwork.collection.toLowerCase().replace(/\s+/g, '-') === collection.toLowerCase().replace(/\s+/g, '-')
  );
};
